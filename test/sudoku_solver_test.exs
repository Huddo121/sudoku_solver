defmodule SudokuSolverTest do
  use ExUnit.Case
  require SudokuSolver


  test "puzzle parsed correctly" do
    puzzle = "..3.2.6..9..3.5..1..18.64....81.29..7.......8..67.82....26.95..8..2.3..9..5.1.3.."
    expected_result = Map.new([ {"A1", "."}, {"A2", "."}, {"A3", "3"}, {"A4", "."}, {"A5", "2"}, {"A6", "."}, {"A7", "6"}, {"A8", "."}, {"A9", "."}, 
                        {"B1", "9"}, {"B2", "."}, {"B3", "."}, {"B4", "3"}, {"B5", "."}, {"B6", "5"}, {"B7", "."}, {"B8", "."}, {"B9", "1"},
                        {"C1", "."}, {"C2", "."}, {"C3", "1"}, {"C4", "8"}, {"C5", "."}, {"C6", "6"}, {"C7", "4"}, {"C8", "."}, {"C9", "."},
                        {"D1", "."}, {"D2", "."}, {"D3", "8"}, {"D4", "1"}, {"D5", "."}, {"D6", "2"}, {"D7", "9"}, {"D8", "."}, {"D9", "."},
                        {"E1", "7"}, {"E2", "."}, {"E3", "."}, {"E4", "."}, {"E5", "."}, {"E6", "."}, {"E7", "."}, {"E8", "."}, {"E9", "8"},
                        {"F1", "."}, {"F2", "."}, {"F3", "6"}, {"F4", "7"}, {"F5", "."}, {"F6", "8"}, {"F7", "2"}, {"F8", "."}, {"F9", "."},
                        {"G1", "."}, {"G2", "."}, {"G3", "2"}, {"G4", "6"}, {"G5", "."}, {"G6", "9"}, {"G7", "5"}, {"G8", "."}, {"G9", "."},
                        {"H1", "8"}, {"H2", "."}, {"H3", "."}, {"H4", "2"}, {"H5", "."}, {"H6", "3"}, {"H7", "."}, {"H8", "."}, {"H9", "9"},
                        {"I1", "."}, {"I2", "."}, {"I3", "5"}, {"I4", "."}, {"I5", "1"}, {"I6", "."}, {"I7", "3"}, {"I8", "."}, {"I9", "."}])
    parsed = SudokuSolver.grid_values(puzzle)
    assert Map.size(parsed) == 81
    assert Map.equal?(expected_result, parsed)
  end
end
